temp = float(input("What is the temperature outside (in °C)? "))

if temp >= 35:
    print("It's a heatwave! Stay inside and have some ice cream.")
elif temp >= 25:
    print("Perfect summer weather! It's a great time for a swim.")
elif temp >= 15:
    print("It's a pleasant day. Maybe go for a walk in the park?")
else:
    print("It doesn't feel much like summer. Better grab a light jacket!")